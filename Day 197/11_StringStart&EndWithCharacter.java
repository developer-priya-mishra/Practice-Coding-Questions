class StringStartEndWithCertainCharacter {
    public static void main(String[] args) {
        String str = "Priya";

        if (str.startsWith("p") == true) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }

        if (str.endsWith("a") == true) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }
    }
}
