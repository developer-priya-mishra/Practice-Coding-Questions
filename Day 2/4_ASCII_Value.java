// ** Using Assigning a Variable to the int Variable**

class Value1 {
    public static void main(String[] args) {
        char ch1 = 'A';
        char ch2 = 'a';

        int ascii1 = ch1;
        int ascii2 = ch2;

        System.out.println("ASCII code of 'A' : " + ascii1);
        System.out.println("ASCII code of 'a' : " + ascii2);

    }
}